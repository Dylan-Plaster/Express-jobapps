/************************************** get */

describe("get", function () {
  test("works", async function () {
    let user = await User.get("u1");
    expect(user).toEqual({
      username: "u1",
      firstName: "U1F",
      lastName: "U1L",
      email: "u1@email.com",
      isAdmin: false,
      jobs: [],
    });
  });

  test("not found if no such user", async function () {
    try {
      await User.get("nope");
      fail();
    } catch (err) {
      expect(err instanceof NotFoundError).toBeTruthy();
    }
  });

  test("works with jobs", async () => {
    let jobs = await db.query("SELECT id FROM jobs LIMIT 1");
    let id = jobs.rows[0].id;
    await User.apply("u1", id);

    let user = await User.get("u1");
    expect(user).toEqual({
      username: "u1",
      firstName: "U1F",
      lastName: "U1L",
      email: "u1@email.com",
      isAdmin: false,
      jobs: [{ id: id, title: "j1", companyHandle: "c1", companyName: "C1" }],
    });

    let jobs2 = await db.query("SELECT id FROM jobs LIMIT 1 OFFSET 1");
    let id2 = jobs2.rows[0].id;
    await User.apply("u1", id2);
    let user2 = await User.get("u1");
    expect(user2).toEqual({
      username: "u1",
      firstName: "U1F",
      lastName: "U1L",
      email: "u1@email.com",
      isAdmin: false,
      jobs: [
        { id: id, title: "j1", companyHandle: "c1", companyName: "C1" },
        { id: id2, title: "j2", companyHandle: "c2", companyName: "C2" },
      ],
    });
  });
});

/************************************apply */
describe("apply", () => {
  test("works", async function () {
    let jobs = await db.query("SELECT id FROM jobs LIMIT 1");
    let id = jobs.rows[0].id;
    let res = await User.apply("u1", id);
    expect(res).toEqual(id);
    let apps = await db.query("SELECT * FROM applications");
    expect(apps.rows.length).toEqual(1);
    expect(apps.rows[0]).toEqual({ username: "u1", job_id: id });
  });

  test("throws notFound for no such user", async () => {
    expect.assertions(1);
    let jobs = await db.query("SELECT id FROM jobs LIMIT 1");
    let id = jobs.rows[0].id;
    try {
      let res = await User.apply("d5", id);
    } catch (err) {
      expect(err instanceof NotFoundError).toBeTruthy();
    }
  });

  test("throws notFound for no such job", async () => {
    expect.assertions(1);
    try {
      let res = await User.apply("u1", 0);
    } catch (err) {
      expect(err instanceof NotFoundError).toBeTruthy();
    }
  });
});

/************************************** update */

describe("update", function () {
  const updateData = {
    firstName: "NewF",
    lastName: "NewF",
    email: "new@email.com",
    isAdmin: true,
  };

  test("works", async function () {
    let job = await User.update("u1", updateData);
    expect(job).toEqual({
      username: "u1",
      ...updateData,
    });
  });

  test("works: set password", async function () {
    let job = await User.update("u1", {
      password: "new",
    });
    expect(job).toEqual({
      username: "u1",
      firstName: "U1F",
      lastName: "U1L",
      email: "u1@email.com",
      isAdmin: false,
    });
    const found = await db.query("SELECT * FROM users WHERE username = 'u1'");
    expect(found.rows.length).toEqual(1);
    expect(found.rows[0].password.startsWith("$2b$")).toEqual(true);
  });

  test("not found if no such user", async function () {
    try {
      await User.update("nope", {
        firstName: "test",
      });
      fail();
    } catch (err) {
      expect(err instanceof NotFoundError).toBeTruthy();
    }
  });

  test("bad request if no data", async function () {
    expect.assertions(1);
    try {
      await User.update("c1", {});
      fail();
    } catch (err) {
      expect(err instanceof BadRequestError).toBeTruthy();
    }
  });
});

